#' Adds two numbers
#'
#' Adds two number together using R
#'
#'
#' @param a numeric
#' @param b numeric
#'
#' @return numeric
#' @export
#' @examples
#' add(5,8)
add <- function(a,b){
  return(a+b)
}

##################################################

#' Subtracts two numbers
#'
#' Subtracts two number together using R
#'
#'
#' @param a numeric
#' @param b numeric
#'
#' @return numeric
#' @export
#' @examples
#' subtract(5,8)
subtract <- function(a,b){
  return(a-b)
}
